package day1;

public class Operator {

	public static void main(String[] args) 
	{
		int a = 70, b= 30;
		
		System.out.println("\n a = "+a+"\t b = "+b);
		System.out.println("\n a + b = "+ (a+b));
		System.out.println("\n a - b = "+ (a-b));
		System.out.println("\n a * b = "+ (a*b));
		System.out.println("\n a / b = "+ (a/b));
		System.out.println("\n a % b = "+ (a%b));

	}

}
